/**
 * Execute a single vote cycle with retry logic
 */
async function voteCycle() {
  logSeparator();
  logger.info(`‚Źį Vote cycle started at ${new Date().toLocaleString('id-ID', { timeZone: 'Asia/Jakarta' })}`);

  let lastError = null;

  for (let attempt = 1; attempt <= config.maxRetries; attempt++) {
    logger.info(`ūüĒĄ Attempt ${attempt}/${config.maxRetries}`);

    try {
      const result = await performVote();

      if (result.success) {
        logger.info('ūüéČ Vote cycle completed successfully!');

        // Send Telegram notification
        if (result.details?.note?.includes('Already submitted')) {
          await notifyAlreadyVoted(result.details.note);
        } else if (result.details?.note) {
          // Informational (waiting, no round, etc.)
          await notifyAlreadyVoted(result.details.note);
        } else {
          await notifyVoteSuccess(result.details);
        }
        await notifyNextVote();
        return;
      }

      // Check if session expired
      if (result.details?.sessionExpired) {
        logger.error('ūüĒĎ Session expired. Need re-import.');
        await notifySessionExpired();
        return;
      }

      lastError = result.details?.error;
      logger.warn(`‚ö†ÔłŹ  Attempt ${attempt} failed: ${lastError}`);

      await notifyVoteFailed({
        ...result.details,
        attempt,
        maxAttempts: config.maxRetries,
        willRetry: attempt < config.maxRetries,
      });
    } catch (err) {
      lastError = err.message;
      logger.error(`ūüí• Attempt ${attempt} crashed: ${err.message}`);

      await notifyVoteFailed({
        error: err.message,
        strategy: config.voteStrategy,
        attempt,
        maxAttempts: config.maxRetries,
        willRetry: attempt < config.maxRetries,
      });
    }

    // Wait before retry (exponential backoff)
    if (attempt < config.maxRetries) {
      const delay = config.retryDelay * attempt;
      logger.info(`‚Ź≥ Waiting ${delay / 1000}s before retry...`);
      await new Promise((resolve) => setTimeout(resolve, delay));
    }
  }

  logger.error(`‚ĚĆ All ${config.maxRetries} attempts failed. Last error: ${lastError}`);
}

